#include "../include/libft.h"

/*
EXAMPLE:
	1111 1111 1111 1000 0000 => ~0x7F
	0000 0000 0000 0010 1010 => 40
*/

int	ft_isascii(int c)
{
	return (!(c & ~0x7F));
}
