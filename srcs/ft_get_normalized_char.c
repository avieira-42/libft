char get_normalized_char(char *s)
{
	char	c;

    if (!*s)
        return (*s);
    if (*s >= '\240' && *s <= '\243')
        c = 'a';
    if (*s == '\200' || *s == '\201' || *s == '\202' || *s <= '\203')
        c = 'A';
    if (*s >= '\250' && *s <= '\252')
        c = 'e';
    if (*s == '\210' || *s == '\211')
        c = 'E';
    if (*s == '\272')
    {
		if (!(*s + 1))
			return (*s + 1);
        if (*(s + 1) == '\275')
            c = 'e';
        if (*(s + 1) == '\274')
            c = 'E';
    }
    if (*s == '\254' || *s == '\255')
        c = 'i';
    if (*s == '\214' || *s == '\215')
        c = 'I';
    if (*s >= '\262' && *s <= '\265')
        c = 'o';
    if (*s >= '\222' && *s <= '\225')
        c = 'O';
    if (*s == '\271' || *s == '\272')
        c = 'u';
    if (*s == '\231' || *s == '\232')
        c = 'U';
    if (*s == '\247')
		c = 'c';
	if (*s == '\207')
        c = 'C';
    return c;
}
